public class TableEntry {

    private String tableName = "";
    private String tableNameJP = "";
    private String className = "";
    private String charTableJP = "";

    public TableEntry() {
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public String getTableNameJP() {
        return tableNameJP;
    }

    public void setTableNameJP(String tableNameJP) {
        this.tableNameJP = tableNameJP;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getCharTableJP() {
        return charTableJP;
    }

    public void setCharTableJP(String charTableJP) {
        this.charTableJP = charTableJP;
    }
}
